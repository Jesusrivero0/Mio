package Ejercicio33;

public class Articulo {

	private String descripcion;
	private Integer precio;
	
	
	public Articulo(String descripcion, Integer precio) {
		this.descripcion = descripcion;
		this.precio = precio;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public Integer getPrecio() {
		return precio;
	}
	
	
	
}
